USE BDX
GO
DROP TABLE IF EXISTS dbo.BO_LOG
GO
CREATE TABLE dbo.BO_LOG
(
	ID BIGINT IDENTITY NOT NULL PRIMARY KEY,
	ID_ENTIDAD SMALLINT NOT NULL,
	ID_EVENTO SMALLINT NOT NULL,
	MENSAJE VARCHAR(MAX),
	IDENTIFICADOR VARCHAR(50) NOT NULL,
	FECHA_REGISTRO DATETIME NOT NULL DEFAULT(GETDATE()),
	CONSTRAINT FK_ID_ENTIDAD FOREIGN KEY (ID_ENTIDAD)
	REFERENCES BO_ENTIDAD_LOG(ID),
	CONSTRAINT FK_ID_EVENTO FOREIGN KEY (ID_EVENTO)
	REFERENCES BO_EVENTS_LOG(ID)
)